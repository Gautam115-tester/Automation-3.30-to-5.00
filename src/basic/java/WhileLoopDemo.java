package basic.java;
public class WhileLoopDemo {
	public static void main(String[] args) {
//		increment
		int i = 1;
		while (i<=100) {
			System.out.println("Hello");
			System.out.println(i);
			i++;
		}
		System.out.println();
//		Decrement
		int j = 10;
		while (j>=1) {
			System.out.println(j);
			j--;
		}	
//		Infinite 
		while (true) {
			System.out.println("hello everyone");
			break;
		}	
//		System.out.println("everyone");
		

		
	}

}
